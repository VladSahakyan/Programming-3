function drawForBackend() {
    for (var i in grassArr) {
        grassArr[i].mul();
    }
    for (var i in grassEaterArr) {
        grassEaterArr[i].eat()
        grassEaterArr[i].move()
        grassEaterArr[i].mult()
        grassEaterArr[i].die()
    }
    for (var i in meatEaterArr) {
        meatEaterArr[i].multi()
        meatEaterArr[i].moved()
        meatEaterArr[i].meal()
        meatEaterArr[i].died()
    }
    for (var i in humanArr) {
        humanArr[i].multip()
        humanArr[i].walk()
        humanArr[i].ate()
        humanArr[i].shoot()
        humanArr[i].gr()
        humanArr[i].beat()
        humanArr[i].dead()
    }
    for (var i in robotArr) {
        robotArr[i].drive()
        robotArr[i].step()
        robotArr[i].destroy()
        robotArr[i].terminate()
        robotArr[i].exterminate()
        robotArr[i].low()
        if (robotArr != undefined && robotArr.length != 0) {
            robotArr[i].batery()
        }
    }


    let sendData = {
        matrix: matrix
    }
    statistics = {
        grasses: grassArr.length,
        grassEaters: grassEaterArr.length,
        meatEaters: meatEaterArr.length,
        humans: humanArr.length,
        robots: robotArr.length,
    }
    fs.writeFileSync('statistics.json', JSON.stringify(statistics, undefined, 2))
    mystatistics = fs.readFileSync('statistics.json').toString()
    io.sockets.emit("sendStatistics", JSON.parse(mystatistics))
    io.sockets.emit("matrix", sendData)
}

setInterval(drawForBackend, 500)
